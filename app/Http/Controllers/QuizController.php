    // Start Quiz of selected Subject
    public function start($subject_id){
        $questions = Question::
            where('subject_id', '=', $subject_id)
            ->inRandomOrder()
            ->take(3)
            ->get();

        //dd($questions->all());

        return response()->json([
            'questions' => $questions
        ]);


        return view('quiz/quiz');

    }

